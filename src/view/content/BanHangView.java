    private  void fillHD(List<HoaDon> lists) {
        model = (DefaultTableModel) tblHoaDon.getModel();
        model.setRowCount(0);
        for (HoaDon item : lists) {
            model.addRow(new Object[] {
                item.getIdHoaDon(),
                item.getTenKH(),
                item.getTenNV(),
                item.isTrangThai() ? "Ðã thanh toán" : "Chờ thanh toán"
            });
        }
   }

   
